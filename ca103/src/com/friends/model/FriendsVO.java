package com.friends.model;

public class FriendsVO {
	private String self_id;
	private String other_id;
	private Integer fr_status;
	private String self_nickname;
	private String self_ac;
	
	public String getSelf_id() {
		return self_id;
	}
	public void setSelf_id(String self_id) {
		this.self_id = self_id;
	}
	public String getOther_id() {
		return other_id;
	}
	public void setOther_id(String other_id) {
		this.other_id = other_id;
	}
	public Integer getFr_status() {
		return fr_status;
	}
	public void setFr_status(Integer fr_status) {
		this.fr_status = fr_status;
	}
	public String getSelf_nickname() {
		return self_nickname;
	}
	public void setSelf_nickname(String self_nickname) {
		this.self_nickname = self_nickname;
	}
	public String getSelf_ac() {
		return self_ac;
	}
	public void setSelf_ac(String self_ac) {
		this.self_ac = self_ac;
	}
	
	

}
